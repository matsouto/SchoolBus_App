class Endereco():

    """
    Representação de um endereco físico.
    """

    def __init__(
        self,
        rua: str,
        numero: int,
        complemento: str,
        bairro: str
    ):
        self.rua = rua,
        self.numero = numero,
        self.complemento = complemento,
        self.bairro = bairro

